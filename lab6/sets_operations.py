# Введіть ваші дані
surname = "Семенчук"
name = "Анатолій"
patronymic = "Анатолійович"

# Створення множин з ПІБ
set1 = set(surname)
set2 = set(name)
set3 = set(patronymic)

# Український алфавіт у нижньому регістрі
ukrainian_alphabet = set("абвгґдежзийіклмнопрстуфхцчшщьюяєії")

# a. Всі літери, які є в set2 і set3
a = set2 & set3
print("a. Всі літери, які є в set2 і set3:", a)

# b. Літери, які є і в set1 або set2
b = set1 | set2
print("b. Літери, які є і в set1 або set2:", b)

# c. Літери set2, яких немає в set1
c = set2 - set1
print("c. Літери set2, яких немає в set1:", c)

# d. Чи є set1 підмножиною set3
d = set1.issubset(set3)
print("d. Чи є set1 підмножиною set3:", d)

# e. Літери, які є в set1 або set2, але не в обидвох
e = set1 ^ set2
print("e. Літери, які є в set1 або set2, але не в обидвох:", e)

# f. Всі літери, які є в двох множинах з трьох
f = (set1 & set2) | (set1 & set3) | (set2 & set3)
print("f. Всі літери, які є в двох множинах з трьох:", f)

# g. Всі літери, які є лише в одній множині з трьох
g = (set1 | set2 | set3) - (set1 & set2) - (set1 & set3) - (set2 & set3)
print("g. Всі літери, які є лише в одній множині з трьох:", g)

# h. Всі літери алфавіту, яких немає в жодній з множин
h = ukrainian_alphabet - (set1 | set2 | set3)
print("h. Всі літери алфавіту, яких немає в жодній з множин:", h)

# i. Всі літери алфавіту, яких немає у кожній з трьох множин
i = ukrainian_alphabet - (set1 & set2 & set3)
print("i. Всі літери алфавіту, яких немає у кожній з трьох множин:", i)
